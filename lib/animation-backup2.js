export async function animateGirl() {
  return new Promise((resolve) => {
    const interval = setInterval(() => {
      process.stdout.write('\x1b[H\x1b[J'); // Clear the screen
      const frame = frames.shift();
      if (!frame) {
        clearInterval(interval);
        resolve();
        return;
      }
      process.stdout.write(gradient(['#fbe8d3', '#c68642', '#a0522d', '#000000'])(frame)); // Colori pelle, capelli, dettagli
    }, 10); // Adjust the interval for frame speed
  });
}

